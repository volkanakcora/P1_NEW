In vi / vim command mode
 a Insert content after the current cursor position.
 A Insert content at the end of the line where the cursor is (end of line).
 i Insert content before the current cursor position.
 I Insert the content at the beginning (the beginning of the line) of the line where the cursor is.
 o Insert a new line after the line under the cursor.
 O Insert a new line before the line under the cursor.

 